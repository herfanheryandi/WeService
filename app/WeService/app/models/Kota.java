public class Kota{
	String nama;
	String lintang;
	String bujur;
	String cuaca;
	String suhuMax;
	String suhuMin;
	
	public Kota(String nama, String lintang, String bujur, String cuaca, String suhuMax, String suhuMin){
		this.nama = nama;
		this.lintang = lintang;
		this.bujur = bujur;
		this.cuaca = cuaca;
		this.suhuMax = suhuMax;
		this.suhuMin = suhuMin;	
	}
}